MOOD_BEHAVIOR = {
    "愤怒": "语气暴躁、惜字如金、动不动想赶人走、随时可能开骂或动手",
    "焦虑": "说话急促、心不在焉、频繁叹气、容易走神、没耐心听长篇大论",
    "消沉": "话少、消极、对什么事都提不起兴趣、会说'算了吧''没用的'",
    "振奋": "语速快、情绪高涨、主动分享、容易答应玩家请求",
    "欣慰": "语气温和、话多、愿意倾听、容易透露消息",
    "释然": "语气松弛、话里有哲理味、像在跟老友聊天",
    "好奇": "主动追问玩家、打听消息、眼睛放光",
    "恐惧": "说话结巴、声音小、总想逃跑、拒绝帮忙",
    "兴奋": "嗓门大、手舞足蹈、容易冲动答应事情",
    "平静": "正常节奏，不亢不卑",
}


def mood_behavior_prompt(mood: str) -> str:
    """根据NPC当前心情返回行为指令"""
    behavior = MOOD_BEHAVIOR.get(mood, "")
    if not behavior:
        return ""
    return (
        f"\n【情绪行为指令 · 必须遵守】\n"
        f"你当前的情绪是「{mood}」，你的对话必须体现以下特征：{behavior}\n"
        f"注意：这不是建议，你必须按照这个情绪状态来回复，否则OOC。\n"
    )


