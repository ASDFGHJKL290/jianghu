def relation_prompt(npc_name, intimacy):
    if intimacy >= 80:
        level = "挚友"
    elif intimacy >= 60:
        level = "好友"
    elif intimacy >= 40:
        level = "相识"
    elif intimacy >= 20:
        level = "初识"
    else:
        level = "陌生"
    return f"【亲密度：{level}（{intimacy}/100）】\n根据亲密度调整语气：陌生人客气，好友随意，挚友可开玩笑。"

